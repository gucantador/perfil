// box 1

var box = document.querySelector("#box");

box.addEventListener("mouseover", function() {

  this.style.backgroundColor = "white";

});



box.addEventListener("mouseout", function() {

  this.style.backgroundColor = "rgb(228, 228, 228)";

});


